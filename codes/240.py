import bisect


class Solution(object):
    def searchMatrix(self, matrix, target):
        """
        :type matrix: List[List[int]]
        :type target: int
        :rtype: bool
        """
        for row in matrix:
            if not row:
                continue
            elif len(row) == 1:
                if row[0] == target:
                    return True
            else:
                if row[bisect.bisect(row, target, 0, len(row)) - 1] == target:
                    return True
        return False


